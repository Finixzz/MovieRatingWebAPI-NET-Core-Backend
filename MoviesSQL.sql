CREATE TABLE Movie
(
	MovieId INT NOT NULL IDENTITY(1,1),
	Title NVARCHAR(50) NOT NULL,
	ImagePath NVARCHAR(200),
	Description NVARCHAR(400) NOT NULL,
	ReleaseDate DATE,
	CONSTRAINT PK_Movies PRIMARY KEY(MovieId)
)

CREATE TABLE Actor
(
	ActorId INT NOT NULL IDENTITY(1,1),
	FirstName NVARCHAR(50) NOT NULL,
	LastName NVARCHAR(50) NOT NULL,
	CONSTRAINT PK_Actors PRIMARY KEY(ActorId)
)

CREATE TABLE Cast
(
	CastId INT NOT NULL IDENTITY(1,1),
	MovieId INT NOT NULL,
	ActorId INT NOT NULL,
	CONSTRAINT PK_Cast PRIMARY KEY(CastId),
	CONSTRAINT FK_Cast_Actors FOREIGN KEY(ActorId) REFERENCES Actor(ActorId),
	CONSTRAINT FK_Cast_Movie FOREIGN KEY(MovieId) REFERENCES Movie(MovieId)
)


CREATE TABLE Rating
(
	RatingId INT NOT NULL IDENTITY(1,1),
	UserId NVARCHAR(450),
	MovieId INT NOT NULL,
	Rating INT CHECK (Rating>=1 AND Rating <=5),
	DateAdded DATETIME,
	CONSTRAINT PK_Ratings PRIMARY KEY(RatingId),
	CONSTRAINT FK_Ratings_Users FOREIGN KEY(UserId) REFERENCES AspNetUsers(Id),
	CONSTRAINT FK_Ratings_Movies FOREIGN KEY(MovieId) REFERENCES Movie(MovieId)
)

