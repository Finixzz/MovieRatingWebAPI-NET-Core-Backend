SELECT TOP 10
	m.MovieId,M.Title,M.ImagePath,M.Description,M.ReleaseDate,
	SUM(convert(decimal(8,2),r.Rating))/COUNT(r.rating) Rating
FROM
	Movie m inner join Rating r
		on m.MovieId=r.MovieId
GROUP BY m.MovieId,M.Title,M.ImagePath,M.Description,M.ReleaseDate
ORDER BY Rating DESC

